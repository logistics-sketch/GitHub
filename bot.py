@bot.callback_query_handler(func=lambda call: True)
def handle_callback(call):
    user_id = call.from_user.id
    data = call.data

    # Підменю — показуємо меню без AI
    SUBMENU_MAP = {
        "back_main":      ("Головне меню 👇",                   main_menu),
        "repair":         ("🔧 Оберіть вид ремонту або послуги:", repair_menu),
        "services":       ("⚙️ Безкоштовні послуги:",            services_menu),
        "other":          ("💬 Оберіть тип звернення:",          other_menu),
        "exchange_menu":  ("🔄 Оберіть причину обміну:",         exchange_menu),
        "warranty_menu":  ("🟢 Оберіть гарантійний кейс:",       warranty_menu),
    }

    if data in SUBMENU_MAP:
        text, menu_fn = SUBMENU_MAP[data]
        try:
            bot.edit_message_reply_markup(call.message.chat.id, call.message.message_id, reply_markup=None)
        except Exception:
            pass
        bot.send_message(call.message.chat.id, text, reply_markup=menu_fn())
        bot.answer_callback_query(call.id)
        return

    # AI відповідь
    prompt = CALLBACK_PROMPTS.get(data)
    if not prompt:
        bot.answer_callback_query(call.id)
        return

    user_contexts[user_id] = []
    add_to_context(user_id, "user", prompt)
    bot.answer_callback_query(call.id)
    bot.send_chat_action(call.message.chat.id, "typing")

    try:
        response = client.messages.create(
            model="claude-haiku-4-5",
            max_tokens=1500,
            system=SYSTEM_PROMPT,
            messages=get_user_context(user_id),
        )
        reply = response.content[0].text
        add_to_context(user_id, "assistant", reply)
        bot.send_message(call.message.chat.id, reply, reply_markup=main_menu())
    except Exception as e:
        bot.send_message(call.message.chat.id, f"❌ Помилка: {str(e)}")


